#!/bin/sh

for line in `docker ps -a | awk '{print $1}' | grep -v "CONTAINER"`
do
   docker stop $line
   docker rm $line
done


for line in `docker images | awk '{print $3}' | grep -v "IMAGE"`
do
   docker rmi -f $line
done

echo ================================================================================================================
docker ps -a
echo ================================================================================================================
docker images
echo ================================================================================================================
docker volume prune
echo ================================================================================================================
